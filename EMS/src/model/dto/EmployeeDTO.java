package model.dto;

/**
 *
 * @author fawad
 */
public class EmployeeDTO {
    private int id;
    private String name;
    private int age;
    private String department;

    public EmployeeDTO() {
    }

    public EmployeeDTO(int id, String name, int age, String department) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.department = department;
    }

    public EmployeeDTO(String name, int age, String department) {
        this.name = name;
        this.age = age;
        this.department = department;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }

    @Override
    public String toString() {
        return "EmployeeDTO{" + "id=" + id + ", name=" + name + ", age=" + age + ", department=" + department + '}';
    }
    
    
}
